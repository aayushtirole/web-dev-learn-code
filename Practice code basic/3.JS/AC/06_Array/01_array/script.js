// Array 

let marks = [90, 80, 70, 60, 50];

console.log(marks); // Output: [90, 80, 70, 60, 50]
console.log(marks[0]); // Output: 90 (Accessing the first element)
console.log(marks.length); // Output: 5 (Length of the array)

// Modifying an element
marks[2] = 75;
console.log(marks); // Output: [90, 80, 75, 60, 50]
// Adding a new element
marks[5] = 40;
console.log(marks); // Output: [90, 80, 75, 60, 50, 40]