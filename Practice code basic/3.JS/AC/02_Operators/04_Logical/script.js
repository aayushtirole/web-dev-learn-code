// Logical Operators in JavaScript

let x = true;
let y = false;

// ---------------- LOGICAL AND ----------------
console.log(x && y);  
// && (Logical AND) → true tabhi hoga jab dono true ho
// true && false → false

console.log(x && true);  
// true && true → true

// ---------------- LOGICAL OR ----------------
console.log(x || y);  
// || (Logical OR) → true tabhi hoga jab ek bhi true ho
// true || false → true

console.log(y || false);  
// false || false → false

// ---------------- LOGICAL NOT ----------------
console.log(!x);  
// ! (Logical NOT) → value ka ulta kar deta hai
// !true → false

console.log(!y);  
// !false → true
