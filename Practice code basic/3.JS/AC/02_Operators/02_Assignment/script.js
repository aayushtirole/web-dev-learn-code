// Assignment Operators in JavaScript

let x = 10;   // = simple assignment (x ko 10 assign kiya)
console.log(x);  // 10

x += 5;       // += addition assignment (x = x + 5)
console.log(x);  // 15

x -= 3;       // -= subtraction assignment (x = x - 3)
console.log(x);  // 12

x *= 2;       // *= multiplication assignment (x = x * 2)
console.log(x);  // 24

x /= 4;       // /= division assignment (x = x / 4)
console.log(x);  // 6

x %= 5;       // %= modulus assignment (x = x % 5) â†’ remainder
console.log(x);  // 1

x **= 3;      // **= exponentiation assignment (x = x ** 3)
console.log(x);  // 1 (kyunki 1^3 = 1)



